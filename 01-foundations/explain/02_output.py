# 🖨️ Print Output


# print() is used to display information on the screen.
# It can show text, numbers, and multiple values.


# Printing text (String)

print("Hello Python")


# Output:
# Hello Python


# Printing numbers

print(25)
print(3.99)


# Output:
# 25
# 3.99


# Printing multiple values

print("Name:", "Ahmed")
print("Age:", 20)


# Output:
# Name: Ahmed
# Age: 20


# New line (\n)
# \n moves the next text to a new line.

print("Hello\nWorld")


# Output:
# Hello
# World


# Tab space (\t)
# \t adds a tab space.

print("Name:\tAhmed")


# Output:
# Name:    Ahmed
