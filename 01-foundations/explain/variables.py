# 📦 Variables

# A variable is used to store information.
# We create a variable using the = symbol.


# Creating variables

name = "Ahmed"
age = 20

print(name)
print(age)


# Output:
# Ahmed
# 20


# Changing a variable value

age = 20
age = 21

print(age)


# Output:
# 21


# Variable naming rules

# Good examples:
first_name = "Ahmed"
user_age = 20

# Avoid starting with numbers:
# 1name = "Ahmed"  ❌


# Variables can store different types of data

city = "Stockholm"   # String (Text)
number = 25          # Integer (Whole number)
price = 9.99         # Float (Decimal number)
student = True        # Boolean (True or False)


print(city)
print(number)
print(price)
print(student)


# Output:
# Stockholm
# 25
# 9.99
# True
